    public void setTableHeader() {
        Object[] tableHeader = viewControler.getHeaderTableTransaksi();
        for (Object tableHeader1 : tableHeader) {
            tableModel.addColumn(tableHeader1);
        }
    }

    public void setIsiDataTransaksi() {
        tableModel.getDataVector().removeAllElements();
        tableModel.fireTableDataChanged();

        ArrayList<Object[]> data = null;
        try {
            data = viewControler.getAllDataTransaksi();
            for (int i = 0; i < data.size(); i++) {
                tableModel.addRow(data.get(i));
            }
            jTable1.getColumnModel().getColumn(11).setCellEditor(new DefaultCellEditor(jComboAmbil));
            jTable1.getColumnModel().getColumn(12).setCellEditor(new DefaultCellEditor(jComboBayar));
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    public void runApps() {
        try {
            this.setTableHeader();
            this.setIsiDataTransaksi();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }


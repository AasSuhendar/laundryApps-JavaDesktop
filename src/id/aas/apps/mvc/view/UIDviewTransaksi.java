    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        // TODO add your handling code here:
        try {
            boolean validasi = false;
            int row = jTable1.getSelectedRow();
            if (row < 0) {
                return;
            }
            String kd_transaksi = jTable1.getValueAt(row, 0).toString();
            String kd_laundry = jTable1.getValueAt(row, 1).toString();
            String keteranganT = jTable1.getValueAt(row, 12).toString();
            String keteranganL = jTable1.getValueAt(row, 11).toString();
            validasi = viewControler.updatePengambilan(kd_laundry, keteranganL, kd_transaksi, keteranganT);
            if (validasi == true) {
                JOptionPane.showMessageDialog(null, "Pengembilan Laundry Berhasil, Data di Update", "Pengambilan Laundry", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Pengembilan Laundry Gagal, Data di Tidak Update", "Pengambilan Laundry", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_button1ActionPerformed


    private void jbtnSimpanEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSimpanEditActionPerformed
        // TODO add your handling code here:
        try {
            boolean validasi = false;
            String nama = jtxtNama.getText();
            String no_tlp = jtxtTlp.getText();
            String alamat = jtxtAreaAlamat.getText();
            String btnStatus = jbtnSimpanEdit.getText();

            if (nama.isEmpty() || no_tlp.isEmpty() || alamat.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Terdapat Field Kosong, harap mengisi semua field", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } else {
                if (btnStatus.equalsIgnoreCase("Simpan")) {
                    String id = null;
                    validasi = pController.olahDataPelanggan(btnStatus, id, nama, no_tlp, alamat);
                } else {
                    int row = jTable.getSelectedRow();
                    String id = jTable.getValueAt(row, 0).toString();
                    validasi = pController.olahDataPelanggan(btnStatus, id, nama, no_tlp, alamat);
                }

                if (validasi == true) {
                    setIsiDataPelanggan();
                    if (btnStatus.equalsIgnoreCase("Simpan")) {
                        JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Disimpan", "Simpan Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Diedit", "Edit Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
                    }

                } else {
                    if (btnStatus.equalsIgnoreCase("Simpan")) {
                        JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Disimpan", "Simpan Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Diedit", "Edit Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                reset();
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jbtnSimpanEditActionPerformed


    private void jbtnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnHapusActionPerformed
        // TODO add your handling code here:
        try {
            int row = jTable.getSelectedRow();
            String id_pelanggan = jTable.getValueAt(row, 0).toString();
            System.out.println(id_pelanggan);

            boolean validasi = pController.hapusDataPelanggan(id_pelanggan);
            if (validasi == true) {
                setIsiDataPelanggan();
                JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Dihapus", "Hapus Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Dihapus", "Hapus Data Pelanggan", JOptionPane.INFORMATION_MESSAGE);
            }
            reset();

        } catch (HeadlessException | SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jbtnHapusActionPerformed

